clc; clear all;
t=linspace(0,1,500);
y1=10*cos(2*pi*1.*t);
y2=20*cos(2*pi*1.*t);
subplot(3,1,1);plot(t,y1,'b'); hold on; plot(t,y2,'g');
title('effect of change in A');
legend('A=10','A=20');
xlabel('time');
ylabel('voltage');
y3=10*cos(2*pi*1.*t);
y4=10*cos(2*pi*5.*t);
subplot(3,1,2);plot(t,y3,'b'); hold on; plot(t,y4,'g');
title('effect of change in f');
legend('f=1','f=5');
xlabel('time');
ylabel('voltage');
y5=10*cos((2*pi*1.*t)+(30*pi/180));
y6=10*cos((2*pi*1.*t)+(60*pi/180));
subplot(3,1,3);plot(t,y5,'b'); hold on; plot(t,y6,'g');
title('effect of change in phase');
legend('phase=30','Phase=60');
xlabel('time');
ylabel('voltage');
